#include "../libft.h"

int			ft_strccmp(char *s1, char *s2, char car)
{
	int		n;

	n = 0;
	while (s1[n] && s2[n] && s1[n] != car && s2[n] != car)
	{
		if (s1[n] != s2[n])
			return (s2[n] - s1[n]);
		n += 1;
	}
	if ((s1[n] == s2[n]) ||
		(!s1[n] && s2[n] == car) ||
		(!s2[n] && s1[n] == car))
		return (0);
	return (s2[n] - s1[n]);
}
