#include "libftprintf.h"

int		get_nb(char *str)
{
	int	n;
	int	nb;

	n = 0;
	nb = 0;
	while (str[n] >= '0' && str[n] <= '9')
	{
		nb *= 10;
		nb += str[n] - '0';
		n += 1;
	}
	return (nb);
}
